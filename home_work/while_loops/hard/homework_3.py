N = int(input(": "))
M = int(input(": "))
n_counter = 1
while n_counter <= N:
    m_counter = 1
    while m_counter <= M :
        m_sum = n_counter * m_counter
        print(n_counter, " X " , m_counter, " = ", m_sum)
        m_counter += 1
    n_counter += 1
    
    
